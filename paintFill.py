def fill(arr, x, y, color):
	filler(arr, x, y, arr[y][x], color)

def filler(arr, x, y, srcColor, newColor):
	if(x < 0 or y < 0 or x >= ARRAY_HEIGHT or y >= ARRAY_HEIGHT):
		return
	else:
		if(arr[y][x] == srcColor):
			arr[y][x] = newColor
			filler(arr, x    , y - 1, srcColor, newColor)
			filler(arr, x    , y + 1, srcColor, newColor)
			filler(arr, x - 1, y    , srcColor, newColor)		
			filler(arr, x + 1, y    , srcColor, newColor)
			return
		else:
			return

